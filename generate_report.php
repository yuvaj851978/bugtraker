<?php
require_once 'config.php';
requireRole('tester');

// Get filter parameters
$status = $_GET['status'] ?? 'all';
$priority = $_GET['priority'] ?? 'all';
$dateFrom = $_GET['date_from'] ?? '';
$dateTo = $_GET['date_to'] ?? '';

// Build query
$whereClause = "WHERE bt.created_by = ?";
$params = [$_SESSION['user_id']];

if ($status !== 'all') {
    $whereClause .= " AND bt.status = ?";
    $params[] = $status;
}

if ($priority !== 'all') {
    $whereClause .= " AND bt.priority = ?";
    $params[] = $priority;
}

if (!empty($dateFrom)) {
    $whereClause .= " AND DATE(bt.created_at) >= ?";
    $params[] = $dateFrom;
}

if (!empty($dateTo)) {
    $whereClause .= " AND DATE(bt.created_at) <= ?";
    $params[] = $dateTo;
}

// Get bugs for report
$bugs = [];
$totalBugs = 0;
$stats = [
    'pending' => 0,
    'in_progress' => 0,
    'fixed' => 0,
    'approved' => 0,
    'rejected' => 0
];

try {
    // Get bugs with creator and developer names
    $stmt = $pdo->prepare("
        SELECT bt.*, 
               u1.name as developer_name,
               u2.name as creator_name
        FROM bug_tickets bt 
        LEFT JOIN users u1 ON bt.assigned_dev_id = u1.id 
        LEFT JOIN users u2 ON bt.created_by = u2.id 
        $whereClause 
        ORDER BY bt.id ASC
    ");
    $stmt->execute($params);
    $bugs = $stmt->fetchAll();
    $totalBugs = count($bugs);
    
    // Calculate stats
    foreach ($bugs as $bug) {
        $stats[$bug['status']]++;
    }
} catch (PDOException $e) {
    $error = 'Failed to generate report';
}

// Handle CSV generation
if (isset($_GET['download']) && $_GET['download'] == 'csv') {
    // Set headers for CSV download
    header('Content-Type: text/csv; charset=utf-8');
    header('Content-Disposition: attachment; filename="bug_report_' . date('Y-m-d') . '.csv"');
    header('Cache-Control: no-cache, no-store, must-revalidate');
    header('Pragma: no-cache');
    header('Expires: 0');

    // Generate CSV content
    $output = fopen('php://output', 'w');

    // Write summary section
    fputcsv($output, ['BUG TRACKING REPORT']);
    fputcsv($output, ['Generated by', $_SESSION['user_name']]);
    fputcsv($output, ['Date', date('F d, Y')]);
    fputcsv($output, []); // Empty row
    fputcsv($output, ['SUMMARY']);
    fputcsv($output, ['Total Bugs', count($bugs)]);
    fputcsv($output, ['Approved', $stats['approved']]);
    fputcsv($output, ['In Progress', $stats['pending'] + $stats['in_progress']]);
    fputcsv($output, ['Rejected', $stats['rejected']]);
    fputcsv($output, []); // Empty row

    // Write bug details header
    fputcsv($output, ['BUG DETAILS']);
    fputcsv($output, [
        'Bug ID', 
        'Date of Creation', 
        'Module', 
        'Submodule', 
        'Developer', 
        'Bug Title', 
        'Bug Description', 
        'Created By', 
        'Bug Priority', 
        'Status', 
        'Updated On'
    ]);

    // Write bug details
    foreach ($bugs as $bug) {
        fputcsv($output, [
            '#' . $bug['id'],
            date('M d, Y', strtotime($bug['created_at'])),
            $bug['module'] ?: 'N/A',
            $bug['submodule'] ?: 'N/A',
            $bug['developer_name'] ?: 'Unassigned',
            $bug['title'],
            substr($bug['description'], 0, 200) . (strlen($bug['description']) > 200 ? '...' : ''),
            $bug['creator_name'] ?: 'Unknown',
            $bug['priority'],
            ucfirst(str_replace('_', ' ', $bug['status'])),
            date('M d, Y', strtotime($bug['updated_at']))
        ]);
    }

    fclose($output);
    exit();
}

